package fc.java.part2;

public class AssignOperTest {
    public static void main(String[] args) {
        // 1식 증가하는 수식을 써보세요
        int a = 1;
        a++;
        ++a;
        a=a+1;
        a+=1;
        System.out.println("a = " + a); // 5
        // 1식 감소하는 수식을 써보세요
        int b = 7;
        b--;
        --b;
        b=b-1;
        b-=1;
        System.out.println("b = " + b);
    }
}
